void main() {

  // ++var - pre increment
  // --var - pre decrement

  // var++ - post increment
  // var-- - post decrement

  int age = 24;


  //Post increment
  print(age++); // This will print the value of age first and then increment therefore printing 24
  print(age); // This will print the incremented value of age which is 25



  //Pre increment
  int age2 = 24;
  print(++age2); // This will increment the value of age first and then print therefore printing 25
  print(age2); // This will print the incremented value of age which is 25
}